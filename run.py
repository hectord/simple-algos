from save import read
import numpy as np


def run(inputs, W1, W2, b1, b2):
    A1 = ((W1 @ inputs).T + b1.reshape(-1).T).copy().T
    A1[A1 < 0.0] = 0.0
    A1 = A1.astype(np.float128)

    A1 = (b2.reshape(-1).T + (W2 @ A1).T).copy().T

    ret = np.exp(A1) / np.sum(np.exp(A1), 0)

    return ret.argmax(0)


